                        <div class="row">
                            <h2 style="text-align: center"><b>b-La pilule progestative</b></h2>

                            C’est un comprimé composé uniquement de progestérone. <br/>
                            Elle perturbe la paroi utérine qui devient lisse et incapable
                            d’accueillir l’œuf, elle apaisait la glaire cervicale pour empêcher les spermatozoïdes
                            de rentrer dans l’utérus, elle bloque l’ovulation. <br/> <br/>

                            <b>Comment utilise-t-on la pilule progestative ?</b> <br/>
                            <ul style="margin-left: 20px;">
                                <li>•	Elle se présente sous la forme de plaquettes de vingt-huit (28) comprimés,</li>
                                <li>•	La femme doit avaler une pilule tous les jours, de préférence à la même heure,</li>
                                <li>•	A la fin de la plaquette, elle commence une autre sans interruption,</li>
                                <li>•	Un oubli de plus de vingt-quatre (24) heures annule l’effet de contraception de la pilule progestative.</li>
                            </ul>
