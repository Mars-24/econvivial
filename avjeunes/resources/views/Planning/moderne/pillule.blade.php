                            <div class="row">
                            <div class="col-sm-12">
                                <h2 style="text-align: center"><b>a- La pilule combinée</b></h2>

                                <p>
                            C’est un comprimé composé de deux hormones (la progestérone et l’oestrogène).
                            Elle s’appelle aussi pilule oestroprogestative.
                        </p>

                                <p>
                                    Elle bloque l’ovulation, elle perturbe la paroi utérine
                                    qui devient lisse et incapable d’accueillir l’œuf, elle épaissit
                                    la glaire cervicale pour empêcher les spermatozoïdes de rentrer dans l’utérus,
                                    elle alentit le transport des spermatozoïdes.
                                </p>

                                <p>
                                    Chaque plaquette de pilule comporte vingt huit (28) comprimés dont vingt et un (21)
                                    comprimés blancs contenant des oestroprogestétatifs et 7 marrons contenant uniquement du fer.
                                    La prise doit commencer entre le premier et le septième jour du cycle.
                                    La femme doit s’assurer qu’elle n’est pas enceinte avant de prendre la pilule.
                                </p>

                                <p>
                                    La femme commence par les pilules blanches en avalant un comprimé chaque jour et de
                                    préférence à la même heure. A la fin des 21 comprimés, elle poursuit sans arrêt avec
                                    un comprimé marron par jour.
                                </p>

                                <p>
                                    En général les règles surviennent pendant la prise des comprimés marron.
                                    Dès qu’une plaquette est finie, il faut entamer une autre comme indiqué plus haut sans
                                    sauter de jour.
                                    En cas d’oubli, si la femme se rappelle avant l’heure de la prise suivante elle
                                    prend immédiatement le comprimé oublié puis le comprimé du jour sera pris à l’heure
                                    habituelle. Si la femme se rappelle après deux prises, elle doit se rendre à la
                                    formation sanitaire pour des conseils.

                                </p>

                                <p>
                                    En cas de vomissement intervenant dans un délai de trois heures après la prise, la femme doit
                                    prendre une pilule de remplacement immédiatement. La pilule du jour prochain sera prise à l’heure prévue.
                                </p>
