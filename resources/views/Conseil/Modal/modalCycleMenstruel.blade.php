        <h2 class="text-center" style="color:#ff4081;">Le cycle menstruel </h2>
        <div class="space-50"></div>
        <div class="row">
            <div class="col-sm-6 wow fadeInLeft" >

                <p style="color:#3F729B;">
                  <i class="fa fa-caret-right" style="font-size: 45px;margin-top: 10px;color:#ff4081;"></i>
                  <ul>
                    <li>
                        Le cycle menstruel va du 1er jour des règles jusqu'au
                        dernier jour qui précède les règles suivantes.
                    </li>

                    <li>
                        Elle peut varier entre 21 à 35 jours. On a pris la durée
                        de 28 jours comme norme pour les documents. Toute
                        femme qui a un cycle régulier (donc à des intervalles
                        identiques) que ce soit de 21, 28 ou 35 jours a un cycle
                        normal.
                    </li>

                    <li>
                        L'ovulation a toujours lieu 14 jours avant le 1er jour
                        des règles suivantes. Les règles durent en moyenne 3
                        à 7 jours.
                    </li>
                </ul>
            </p>

            <p>
              <h4 style="color: #007E33;"> <i class="fa fa-star"></i>DIFFÉRENTES PHASES
              DU CYCLE MENSTRUEL</h4>
              Le cycle menstruel est divisé en 3 phases (cas du cycle de 28
              jours).
              <p>
                <b>La 1ère phase ou la phase pré-ovulatoire</b> <br/>

                <p>
                    Encore appelée la phase folliculaire ou oestrogénique, elle va du 1er
                    jour l'ovulation (le 14ème jour dans le cas du cycle de 28 jours). Au
                    début de cette phase, le cerveau donne des directives à un de ses
                    organes qui secrète une substance stimulante (hormone : FSH =
                    hormone folliculaire stimulante) des ovaires. Dans les ovaires
                    certains éléments appelés follicules (contenant chacun un ovule
                    prématuré) commencent à croître.
                </p>

                <p>
                  Dans la plupart des cas, un seul follicule arrive à pleine maturité, on
                  l'appelle le follicule de Graaf qui secrète une autre hormone
                  (oestrogène) qui a trois effets :
                  <ul>
                    <li>
                        Elle stimule la prolifération de l'endomètre (et donc les cellules
                        de l'endomètre se divisent et la muqueuse se reconstitue),
                    </li>

                    <li>
                        Elle prépare le col et la glaire cervicale pour faciliter la
                        pénétration des spermatozoïdes. La glaire cervicale devient
                        plus abondante, plus transparente et plus filante. C'est au
                        moment de l'ovulation que la glaire cervicale est la plus
                        perméable aux spermatozoïdes,
                    </li>

                    <li>
                        Elle stimule l'hypophyse qui envoie l'hormone LH (hormone
                        lutéinisante) qui à son tour favorise l'expulsion de l'ovule.
                    </li>
                </ul>  
            </p>
        </p>
          

    </ul>   
</p>

<p>
 <b> La 2ème phase ou la phase ovulatoire </b> <br/>
 C'est la phase de ponte ovulaire ou ovulation qui survient 14 jours
 avant la fin du cycle quel qu'il soit.  <br/>

 <b>La 3ème phase ou la phase post ovulatoire</b>

 <p>
    La phase progestative, sécrétoire ou proliférative, va de l'ovulation à
    la fin du cycle ( Par exemple du 14ème jour au 28ème jour du cycle
    quand on prend un cycle de 28 jours).
</p>

<p>
    Le follicule, ayant relâché l'ovule, se transforme en corps jaune. Le
    corps jaune secrète une quantité croissante de progestérone et moins d'oestrogène.
</p>
</p>


